# Programming languages offer a method of storing data for reuse.
# If there is a greeting we want to present, a date we need to reuse,
# or a user ID we need to remember we can create a variable
# which can store a value. In Python, we assign variables by using the equals sign (=).
# Variables can’t have spaces or symbols in their names other than an underscore (_).
# They can’t begin with numbers but they can have numbers after the first letter
# (e.g., cool_variable_5 is OK).
#  If the context of a program changes, we can update a variable but perform the same
#  logical process on it.

# We've defined the variable "meal" here to the name of the food we ate for breakfast!
meal = "An english muffin"

# Printing out breakfast
print("Breakfast:")
print(meal)

# Now update meal to be lunch!
meal = "A Pizza"

# Printing out lunch
print("Lunch:")
print(meal)

# Now update "meal" to be dinner
meal = "A bowl of greek yoghurt"

# Printing out dinner
print("Dinner:")
print(meal)
